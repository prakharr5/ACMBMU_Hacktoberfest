#include <iostream>

int main() {
    // Declare and initialize two numbers
    int num1 = 5;
    int num2 = 10;

    // Calculate the sum
    int sum = num1 + num2;

    // Display the result
    std::cout << "The sum of " << num1 << " and " << num2 << " is: " << sum << std::endl;

    return 0;
}
